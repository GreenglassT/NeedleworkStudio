/* ── Vector PDF Export ───────────────────────────────────────── */
/* Renders symbols as selectable TrueType text (DejaVu Sans) and */
/* gridlines as vector paths for cross-stitch reader apps.       */
/* Requires: jsPDF (window.jspdf), pdf-font.js (PDF_FONT_BASE64 */
/*           + PDF_FONT_NAME), pdf-font-garamond.js              */
/*           (PDF_GARAMOND_REGULAR + PDF_GARAMOND_SEMIBOLD),     */
/*           contrastColor(), drawStitch(), drawStitchFabric(),  */
/*           fmtStitches() from utils.js                         */

/* ── Layout constants ──────────────────────────────────────────── */
const _PDF = Object.freeze({
    CELL: 2.5,                          // mm per stitch cell
    ML: 20, MR: 15, MT: 15, MB: 15,    // margins (mm)
    PW: 210, PH: 297,                   // A4 page size (mm)
    ROW_LBL: 12,                        // row label area width (mm)
    COL_LBL: 8,                         // column label area height (mm)
    LEG_ROW_H: 7,                       // legend row height (mm)
    LEG_START_Y: 42,                    // first legend row y position
    FOOTER_Y: 287,                      // footer baseline y
});

let _PDF_BODY_FONT = 'helvetica';  /* upgraded to Garamond at runtime if font loads */

/* Derived: how many cells fit in one tile page */
const _PDF_COLS_PER_PAGE = Math.floor(
    (_PDF.PW - _PDF.ML - _PDF.MR - _PDF.ROW_LBL) / _PDF.CELL
);
const _PDF_ROWS_PER_PAGE = Math.floor(
    (_PDF.PH - _PDF.MT - _PDF.MB - _PDF.COL_LBL) / _PDF.CELL
);

/* ── Footer ────────────────────────────────────────────────────── */
function _drawFooter(doc, pageNum, totalPages) {
    const yr = new Date().getFullYear();
    doc.setFontSize(9);
    doc.setFont(_PDF_BODY_FONT, 'normal');
    doc.setTextColor(120, 100, 80);
    doc.text('\u00a9' + yr, _PDF.ML, _PDF.FOOTER_Y);
    const ftText = 'Generated by Needlework Studio';
    doc.text(ftText, _PDF.PW / 2, _PDF.FOOTER_Y, { align: 'center' });
    const ftW = doc.getTextWidth(ftText);
    doc.link(_PDF.PW / 2 - ftW / 2, _PDF.FOOTER_Y - 3, ftW, 5,
        { url: 'https://needlework-studio.com' });
    doc.text(pageNum + ' / ' + totalPages, _PDF.PW - _PDF.MR, _PDF.FOOTER_Y, { align: 'right' });
    doc.setTextColor(40, 30, 20);
}

/* ── Logo rendering ────────────────────────────────────────────── */
async function _renderLogo() {
    try {
        const resp = await fetch('/static/logo.svg');
        let svg = await resp.text();
        /* Swap light text fill for dark (print-friendly on white paper) */
        svg = svg.replace('fill="#e8dcc8"', 'fill="#2a1f14"');
        /* Crop viewBox to visual content (original 360-wide has dead space on right) */
        svg = svg.replace('viewBox="0 0 360 80"', 'viewBox="0 0 290 80"');
        const blob = new Blob([svg], { type: 'image/svg+xml' });
        const url = URL.createObjectURL(blob);
        return await new Promise((resolve) => {
            const img = new Image();
            img.onload = () => {
                const c = document.createElement('canvas');
                c.width = 580; c.height = 160;
                c.getContext('2d').drawImage(img, 0, 0, 580, 160);
                URL.revokeObjectURL(url);
                resolve(c.toDataURL('image/png'));
            };
            img.onerror = () => { URL.revokeObjectURL(url); resolve(null); };
            img.src = url;
        });
    } catch (e) { return null; }
}

/* ── Cover page ────────────────────────────────────────────────── */
function _drawCoverPage(doc, patternName, patternData, lookup, logoDataUrl) {
    const { grid, grid_w, grid_h } = patternData;
    const fabColor = patternData.fabric_color || '#F5F0E8';
    let curY = _PDF.MT;

    /* Logo */
    if (logoDataUrl) {
        const logoW = 65, logoH = 17.9;  /* 580:160 = 3.625:1 aspect ratio */
        doc.addImage(logoDataUrl, 'PNG',
            (_PDF.PW - logoW) / 2, curY, logoW, logoH, undefined, 'FAST');
        curY += logoH + 4;
    }

    /* Website link */
    doc.setFont(_PDF_BODY_FONT, 'italic');
    doc.setFontSize(8);
    doc.setTextColor(120, 100, 80);
    const siteText = 'needlework-studio.com';
    doc.text(siteText, _PDF.PW / 2, curY + 2, { align: 'center' });
    const siteW = doc.getTextWidth(siteText);
    doc.link(_PDF.PW / 2 - siteW / 2, curY - 1, siteW, 5,
        { url: 'https://needlework-studio.com' });
    curY += 14;

    /* Title block */
    doc.setTextColor(40, 30, 20);
    doc.setFont(_PDF_BODY_FONT, 'bold');
    doc.setFontSize(28);
    doc.text(patternName, _PDF.PW / 2, curY + 4, { align: 'center' });
    curY += 16;
    doc.setFont(_PDF_BODY_FONT, 'normal');
    doc.setFontSize(14);
    doc.text('Cross stitch chart', _PDF.PW / 2, curY, { align: 'center' });
    curY += 7;
    doc.setDrawColor(180, 150, 100);
    doc.setLineWidth(0.3);
    doc.line(_PDF.ML + 20, curY, _PDF.PW - _PDF.MR - 20, curY);
    doc.setLineWidth(0.2);
    curY += 7;

    /* Raster preview — NO grid lines (prevents PK from mis-detecting cover) */
    const prevCellPx = 19;
    const prevCanvas = document.createElement('canvas');
    prevCanvas.width  = grid_w * prevCellPx;
    prevCanvas.height = grid_h * prevCellPx;
    const prevCtx = prevCanvas.getContext('2d');

    /* Base fill + fabric texture */
    prevCtx.fillStyle = fabColor;
    prevCtx.fillRect(0, 0, prevCanvas.width, prevCanvas.height);
    if (typeof drawStitchFabric === 'function') {
        drawStitchFabric(prevCtx, prevCanvas.width, prevCanvas.height,
            prevCellPx, grid_w, grid_h, fabColor);
    }

    /* Stitches */
    for (let r = 0; r < grid_h; r++) {
        for (let c = 0; c < grid_w; c++) {
            const dmc = grid[r * grid_w + c];
            if (dmc === 'BG') continue;
            const hex = (lookup[dmc] || { hex: '#888888' }).hex;
            if (typeof drawStitch === 'function') {
                drawStitch(prevCtx, c * prevCellPx, r * prevCellPx, prevCellPx, hex, fabColor);
            } else {
                prevCtx.fillStyle = hex;
                prevCtx.fillRect(c * prevCellPx, r * prevCellPx, prevCellPx, prevCellPx);
            }
        }
    }

    /* Overdraw BG cells with fabric color */
    for (let r = 0; r < grid_h; r++) {
        for (let c = 0; c < grid_w; c++) {
            if (grid[r * grid_w + c] === 'BG') {
                prevCtx.fillStyle = fabColor;
                prevCtx.fillRect(c * prevCellPx, r * prevCellPx, prevCellPx, prevCellPx);
            }
        }
    }

    /* Fit preview into 150 x 80 mm box */
    const boxW = 150, boxH = 80;
    const asp = grid_h / grid_w;
    let iW = boxW, iH = boxW * asp;
    if (iH > boxH) { iH = boxH; iW = boxH / asp; }
    const iX = (_PDF.PW - iW) / 2;
    doc.addImage(prevCanvas.toDataURL('image/png'), 'PNG',
        iX, curY, iW, iH, undefined, 'FAST');
    const prevBot = curY + iH;

    /* Info text below preview */
    doc.setFont(_PDF_BODY_FONT, 'normal');
    doc.setFontSize(12);
    doc.setTextColor(40, 30, 20);
    doc.text('Design size: ' + grid_w + ' \u00d7 ' + grid_h + ' stitches',
        _PDF.PW / 2, prevBot + 8, { align: 'center' });

    /* Color count */
    doc.setFontSize(12);
    doc.setTextColor(40, 30, 20);
    const brand = patternData.brand || 'DMC';
    const colorCount = (patternData.legend || []).filter(e => e.dmc !== 'BG').length;
    doc.text(colorCount + ' colors (' + brand + ')',
        _PDF.PW / 2, prevBot + 17, { align: 'center' });
}

/* ── Legend headers ─────────────────────────────────────────────── */
/* Column positions (mm from left edge) */
const _LEG_COL = Object.freeze({
    SYM:     24,    // Symbol (center of swatch+symbol area)
    NUM:     42,    // Number (DMC #)
    NAME:    58,    // Name
    COUNT:   175,   // Count (right-aligned)
});

function _drawLegendHeaders(doc, y) {
    doc.setFont(_PDF_BODY_FONT, 'bold');
    doc.setFontSize(9);
    doc.setTextColor(40, 30, 20);
    doc.text('Symbol',  _LEG_COL.SYM, y, { align: 'center' });
    doc.text('Number',  _LEG_COL.NUM, y);
    doc.text('Name',    _LEG_COL.NAME, y);
    doc.text('Count',   _LEG_COL.COUNT, y, { align: 'right' });
    doc.setDrawColor(150, 130, 100);
    doc.line(_PDF.ML, y + 3, _PDF.PW - _PDF.MR, y + 3);
}

/* ── Chart tile rendering (vector) ─────────────────────────────── */
function _drawChartTile(doc, startCol, startRow, tileW, tileH,
                        grid, grid_w, lookup) {
    const C   = _PDF.CELL;
    const ox  = _PDF.ML + _PDF.ROW_LBL;   // chart origin X (mm)
    const oy  = _PDF.MT + _PDF.COL_LBL;   // chart origin Y (mm)

    /* Symbol font size: ~60% of cell for good fit */
    const symPt = C * 0.62 * (72 / 25.4); // mm → pt
    /* Label font size (small) */
    const lblPt = 5;

    /* ── 1. Cell fills + symbols ────────────────────────────────── */
    doc.setFont(PDF_FONT_NAME);
    doc.setFontSize(symPt);

    for (let j = 0; j < tileH; j++) {
        for (let i = 0; i < tileW; i++) {
            const absCol = startCol + i;
            const absRow = startRow + j;
            const dmc = grid[absRow * grid_w + absCol];
            if (dmc === 'BG') continue;

            const info = lookup[dmc] || { hex: '#888888', symbol: '?' };
            const x = ox + i * C;
            const y = oy + j * C;

            /* Colored cell fill */
            doc.setFillColor(info.r, info.g, info.b);
            doc.rect(x, y, C, C, 'F');

            /* Symbol as selectable text */
            doc.setTextColor(info.tr, info.tg, info.tb);
            doc.text(info.symbol, x + C / 2, y + C * 0.7, { align: 'center' });
        }
    }

    /* ── 2. Grid lines (vector paths) ──────────────────────────── */
    /* Light lines: every cell boundary */
    doc.setDrawColor(180, 180, 180);
    doc.setLineWidth(0.1);
    for (let i = 0; i <= tileW; i++) {
        const x = ox + i * C;
        doc.line(x, oy, x, oy + tileH * C);
    }
    for (let j = 0; j <= tileH; j++) {
        const y = oy + j * C;
        doc.line(ox, y, ox + tileW * C, y);
    }

    /* Bold lines: every 10th row/column */
    doc.setDrawColor(80, 80, 80);
    doc.setLineWidth(0.3);
    for (let i = 0; i <= tileW; i++) {
        if ((startCol + i) % 10 === 0) {
            const x = ox + i * C;
            doc.line(x, oy, x, oy + tileH * C);
        }
    }
    for (let j = 0; j <= tileH; j++) {
        if ((startRow + j) % 10 === 0) {
            const y = oy + j * C;
            doc.line(ox, y, ox + tileW * C, y);
        }
    }

    /* ── 3. Row + column labels ────────────────────────────────── */
    doc.setFont(_PDF_BODY_FONT, 'normal');
    doc.setFontSize(lblPt);
    doc.setTextColor(120, 120, 120);

    /* Column labels (top, centered in cell) */
    for (let i = 0; i < tileW; i++) {
        const absCol = startCol + i;
        if (absCol % 10 === 0) {
            doc.text(String(absCol),
                ox + i * C + C / 2,
                _PDF.MT + _PDF.COL_LBL * 0.6,
                { align: 'center' });
        }
    }

    /* Row labels (left side, right-aligned) */
    for (let j = 0; j < tileH; j++) {
        const absRow = startRow + j;
        if (absRow % 10 === 0) {
            doc.text(String(absRow),
                _PDF.ML + _PDF.ROW_LBL - 2,
                oy + j * C + C * 0.7,
                { align: 'right' });
        }
    }
}

/* ── Main export function ──────────────────────────────────────── */
/**
 * Generate and save a PDF chart for a cross-stitch pattern.
 * Output uses vector gridlines, selectable TrueType symbols, and a
 * standard legend format compatible with cross-stitch reader apps.
 *
 * @param {string} patternName  — Title for cover page and filename.
 * @param {Object} patternData  — { grid, grid_w, grid_h, legend }.
 * @param {Object} [opts]       — Optional overrides.
 * @param {boolean} [opts.skipBG]       — Accepted for compat (BG is always skipped).
 * @param {number}  [opts.symbolScale]  — Accepted for compat (ignored; auto-computed).
 * @param {boolean} [opts.coverPage]    — Set false to skip cover page. Default true.
 * @param {function} [opts.onProgress]  — Called with (current, total) tile counts.
 */
async function generatePatternPDF(patternName, patternData, opts) {
    const { jsPDF } = window.jspdf;
    const { grid, grid_w, grid_h, legend } = patternData;

    /* Accept legacy opts without error */
    // opts.skipBG — BG cells are always skipped in vector mode
    // opts.symbolScale — symbol size is auto-computed from cell size
    const onProgress  = opts?.onProgress;
    const showCover   = opts?.coverPage !== false;

    /* Color/symbol lookup (pre-compute RGB to avoid repeated parseInt in hot loops) */
    const lookup = {};
    for (const e of legend) {
        const h = e.hex || '#888888';
        const tc = contrastColor(h);
        lookup[e.dmc] = {
            hex: h, symbol: e.symbol || '?',
            r: parseInt(h.slice(1, 3), 16),
            g: parseInt(h.slice(3, 5), 16),
            b: parseInt(h.slice(5, 7), 16),
            tr: parseInt(tc.slice(1, 3), 16),
            tg: parseInt(tc.slice(3, 5), 16),
            tb: parseInt(tc.slice(5, 7), 16),
        };
    }

    /* Sort legend by stitch count descending */
    const legendEntries = legend.slice().sort((a, b) => (b.stitches || 0) - (a.stitches || 0));

    /* ── Tile math ─────────────────────────────────────────────── */
    const colsPerPage = _PDF_COLS_PER_PAGE;
    const rowsPerPage = _PDF_ROWS_PER_PAGE;
    const colTiles = Math.ceil(grid_w / colsPerPage);
    const rowTiles = Math.ceil(grid_h / rowsPerPage);

    /* ── Legend page count ──────────────────────────────────────── */
    let legendPageCount = 1;
    {
        let y = _PDF.LEG_START_Y;
        for (const e of legendEntries) {
            if (y > 270) { legendPageCount++; y = _PDF.LEG_START_Y; }
            y += _PDF.LEG_ROW_H;
        }
    }

    /* ── Total pages ───────────────────────────────────────────── */
    const chartTileCount = rowTiles * colTiles;
    const totalPages = (showCover ? 1 : 0) + legendPageCount + chartTileCount;

    /* ── Render logo ───────────────────────────────────────────── */
    const logoDataUrl = showCover ? await _renderLogo() : null;

    /* ── Create PDF ────────────────────────────────────────────── */
    const doc = new jsPDF({
        orientation: 'portrait',
        unit: 'mm',
        format: 'a4',
        compress: true,
    });

    /* ── Embed DejaVu Sans font for stitch symbols ────────────── */
    if (typeof PDF_FONT_BASE64 !== 'undefined' && typeof PDF_FONT_NAME !== 'undefined') {
        doc.addFileToVFS(PDF_FONT_NAME + '.ttf', PDF_FONT_BASE64);
        doc.addFont(PDF_FONT_NAME + '.ttf', PDF_FONT_NAME, 'normal');
    }

    /* ── Embed Cormorant Garamond for body text ─────────────── */
    try {
        if (typeof PDF_GARAMOND_REGULAR !== 'undefined' &&
            typeof PDF_GARAMOND_SEMIBOLD !== 'undefined') {
            const gName = 'CormorantGaramond';
            doc.addFileToVFS(gName + '-Regular.ttf', PDF_GARAMOND_REGULAR);
            doc.addFont(gName + '-Regular.ttf', gName, 'normal');
            doc.addFileToVFS(gName + '-SemiBold.ttf', PDF_GARAMOND_SEMIBOLD);
            doc.addFont(gName + '-SemiBold.ttf', gName, 'bold');
            /* Smoke-test: try getting text width to verify metrics loaded */
            doc.setFont(gName, 'normal');
            doc.getTextWidth('test');
            _PDF_BODY_FONT = gName;
        }
    } catch (e) {
        console.warn('Garamond font failed, using helvetica:', e.message);
        _PDF_BODY_FONT = 'helvetica';
    }

    let pageNum = 0;

    /* ── PAGE 1: COVER ─────────────────────────────────────────── */
    if (showCover) {
        pageNum++;
        _drawCoverPage(doc, patternName, patternData, lookup, logoDataUrl);
        _drawFooter(doc, pageNum, totalPages);
    }

    /* ── CHART TILE PAGES ──────────────────────────────────────── */
    if (onProgress) onProgress(0, chartTileCount);

    for (let rowTile = 0; rowTile < rowTiles; rowTile++) {
        for (let colTile = 0; colTile < colTiles; colTile++) {
            if (showCover || rowTile > 0 || colTile > 0) {
                doc.addPage();
            }
            pageNum++;

            /* Compute tile bounds with 1-cell overlap for non-first tiles */
            let sc = colTile * colsPerPage;
            let sr = rowTile * rowsPerPage;
            if (colTile > 0) sc -= 1;  // 1-column overlap
            if (rowTile > 0) sr -= 1;  // 1-row overlap

            const tw = Math.min(
                (colTile > 0 ? colsPerPage + 1 : colsPerPage),
                grid_w - sc
            );
            const th = Math.min(
                (rowTile > 0 ? rowsPerPage + 1 : rowsPerPage),
                grid_h - sr
            );

            _drawChartTile(doc, sc, sr, tw, th, grid, grid_w, lookup);
            _drawFooter(doc, pageNum, totalPages);

            /* Yield to browser so UI can repaint progress */
            if (onProgress) onProgress(rowTile * colTiles + colTile + 1, chartTileCount);
            await new Promise(r => setTimeout(r, 0));
        }
    }

    /* ── LEGEND PAGES ──────────────────────────────────────────── */
    doc.addPage();
    pageNum++;

    /* Title line on first legend page */
    doc.setFont(_PDF_BODY_FONT, 'bold');
    doc.setFontSize(11);
    doc.setTextColor(40, 30, 20);
    doc.text('Thread Legend',
        _PDF.PW / 2, 22, { align: 'center' });

    _drawLegendHeaders(doc, 32);

    let yL = _PDF.LEG_START_Y;
    for (let i = 0; i < legendEntries.length; i++) {
        if (yL > 270) {
            _drawFooter(doc, pageNum, totalPages);
            doc.addPage();
            pageNum++;
            _drawLegendHeaders(doc, 20);
            yL = 28;
        }

        const e = legendEntries[i];
        const info = lookup[e.dmc] || { r: 136, g: 136, b: 136, tr: 0, tg: 0, tb: 0, symbol: '?' };

        /* Plain symbol (black on white) */
        doc.setTextColor(40, 30, 20);
        doc.setFont(PDF_FONT_NAME);
        doc.setFontSize(10);
        doc.text(info.symbol, _LEG_COL.SYM - 3, yL - 1.1, { align: 'center' });

        /* Color swatch with symbol overlay */
        const swX = _LEG_COL.SYM + 2;
        doc.setFillColor(info.r, info.g, info.b);
        doc.rect(swX, yL - 4.5, 5, 5, 'F');
        doc.setDrawColor(160, 160, 160);
        doc.setLineWidth(0.15);
        doc.rect(swX, yL - 4.5, 5, 5, 'S');

        /* Symbol on swatch (contrast color) */
        doc.setTextColor(info.tr, info.tg, info.tb);
        doc.setFont(PDF_FONT_NAME);
        doc.setFontSize(9);
        doc.text(info.symbol, swX + 2.5, yL - 1.1, { align: 'center' });

        /* Number, Name, Count */
        doc.setFont(_PDF_BODY_FONT, 'normal');
        doc.setFontSize(9);
        doc.setTextColor(40, 30, 20);
        doc.text(String(e.dmc || '\u2014'), _LEG_COL.NUM, yL - 1.1);
        doc.text((e.name || '\u2014').slice(0, 40), _LEG_COL.NAME, yL - 1.1);
        doc.text(fmtStitches(e.stitches || 0), _LEG_COL.COUNT, yL - 1.1, { align: 'right' });

        yL += _PDF.LEG_ROW_H;
    }
    _drawFooter(doc, pageNum, totalPages);

    /* ── Save ──────────────────────────────────────────────────── */
    const filename = (patternName.replace(/[^a-zA-Z0-9_\-]/g, '_') || 'pattern') + '.pdf';
    doc.save(filename);
}
