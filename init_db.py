def init_database():
    """Initialize the database with schema and data."""
    from anchor_threads import ANCHOR_THREADS

    # Remove existing database if present
    if os.path.exists(DB_PATH):
        os.remove(DB_PATH)

    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()

    # Create schema
    with open(SCHEMA_PATH, 'r') as f:
        cursor.executescript(f.read())

    # Insert DMC thread data
    cursor.executemany(
        "INSERT INTO threads (number, name, category, hex_color, brand) VALUES (?, ?, ?, ?, 'DMC')",
        DMC_THREADS
    )

    # Insert Anchor thread data (all Standard category)
    cursor.executemany(
        "INSERT INTO threads (number, name, category, hex_color, brand) VALUES (?, ?, 'Standard', ?, 'Anchor')",
        ANCHOR_THREADS
    )

    conn.commit()
    print(f"Database initialized with {len(DMC_THREADS)} DMC + {len(ANCHOR_THREADS)} Anchor = {len(DMC_THREADS) + len(ANCHOR_THREADS)} threads")
    conn.close()


def init_database_if_needed(db_path=None, schema_path=None):
    """Non-interactive init: create DB only if it does not exist.

    Used by desktop mode auto-initialization. Never deletes existing data.
    """
    db = db_path or DB_PATH
    schema = schema_path or SCHEMA_PATH
    if os.path.exists(db):
        return False  # already exists
    from anchor_threads import ANCHOR_THREADS
    os.makedirs(os.path.dirname(db) or '.', exist_ok=True)
    conn = sqlite3.connect(db)
    cursor = conn.cursor()
    with open(schema, 'r') as f:
        cursor.executescript(f.read())
    cursor.executemany(
        "INSERT INTO threads (number, name, category, hex_color, brand) VALUES (?, ?, ?, ?, 'DMC')",
        DMC_THREADS
    )
    cursor.executemany(
        "INSERT INTO threads (number, name, category, hex_color, brand) VALUES (?, ?, 'Standard', ?, 'Anchor')",
        ANCHOR_THREADS
    )
    conn.commit()
    conn.close()
    return True  # created


